// Declare dependencies
const { request } = require('./../testing');

// Declare mocks spec
const mocks = require('./permission.mocks');
const { errorResponse } = require('./../../utils/errors');

// Setting counter
let counter = 0;
function getCounter() {
  counter += 1;
  return counter < 10 ? `0${counter}` : counter;
}

